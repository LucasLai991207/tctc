function tag_completed_chapter(difficulty) {
    const progress = JSON.parse(localStorage.getItem("stage_progress")) || {};

    const allChapters = Level_Data[difficulty].chapter;

    allChapters.forEach(chapter => {
        
        const isChapterAllCompleted = chapter.stage.every(stage => progress[stage.id] === true);

        if (isChapterAllCompleted) {
            console.log(`${chapter.name} 完全通過`);
            
            const current_stage = document.getElementById(`chapter-${chapter.id}`)

            if(!current_stage) return;
            console.log(current_stage)
            current_stage.classList.add("chapter_frame_completed")
        } else {
            console.log(`${chapter.name} 未通過。`);
        }
    });
}



function render_stage(difficulty, chapter){             //渲染關卡
    const single_container = document.getElementById("main_lobby_level_container")
    single_container.innerHTML = ""

    if(!chapter.stage){
        console.log("找不到該難度的關卡 難度：", difficulty)
        return
    }
    //讀檔json
    const progress = JSON.parse(localStorage.getItem("stage_progress")) || {}


    chapter.stage.forEach(function(stage){
        const div = document.createElement("div")
        div.id = `stage-${stage.id}` //標記上ｉｄ
        div.classList.add("main_lobby_map_stage")

        const is_hard = difficulty === "hard"
        const is_completed = progress[stage.id] === true

        if(is_hard) div.classList.add("has_limit")           //高級關卡box要留高一點，放得下wpm/acc門檻
        if(is_completed) div.classList.add("main_lobby_map_stage_completed")

        let html = `
                <div class="main_lobby_map_stage_text1">${stage.id}</div>
                <div class="main_lobby_map_stage_text2">
                    <p class="main_lobby_map_stage_text2-1">
                        -${stage.name}<span class="main_lobby_map_stage_text2-2">${stage.name2}</span>
                    </p>
                </div>
                <div class="main_lobby_map_stage_text3">${stage.type}</div>
                <div class="main_lobby_map_stage_top_right">
                    <div class="main_lobby_map_stage_difficulty_star">${stage.level}</div>
                    
                </div>
                ${is_completed ? `<div class="main_lobby_stage_completed_tag">已完成</div>` : ""}
                `

        //高級關卡才有速度/正確率門檻，改用兩顆小藥丸徽章顯示，不再擠成一整行
        if(is_hard){
            html += `
                <div class="main_lobby_stage_limit">
                    <span class="main_lobby_stage_limit_chip">最低 ${stage.wpm_min} WPM</span>
                    <span class="main_lobby_stage_limit_chip">🎯 正確率 ${stage.acc_min}%</span>
                </div>
                `
        }

        div.innerHTML = html

        div.addEventListener("click", function(){
            window.location.href = `game.html?stage=${stage.id}`
        })
        single_container.appendChild(div)
    })
}

function get_difficulty_by_stageid(stage_id){
    const first_number = String(stage_id).charAt(0);

    if(first_number === "1"){return "easy"}
    if(first_number === "2"){return "medium"}
    if(first_number === "3"){return "hard"}

    return null;
}

